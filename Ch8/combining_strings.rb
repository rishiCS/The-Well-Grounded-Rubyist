# Combining Strings

>> "a" +  "b"
=> "ab"

>> "a" + "b" + "c"
=> "abc"

>> str = "Hi"
=> "Hi"

>> str + "there."
=> "Hi there."

>>str
=> "Hi"

str = "Hi"
=> "Hi"

str << "there."
=> "Hi there."

>> str
=> "Hi there."


# String combination via interpolation

str = "Hi"
=> "Hi"

"#{str} there."
=> "Hi there."

